            <p>At RewardsX, we respect your privacy and are committed to giving you control over your personal data. If you no longer wish to use your RewardsX account and would like to delete it, you have the option to permanently delete your account and associated data. This policy explains the process for account deletion and what happens to your data when you request deletion.</p>

            <section id="section2">
                <h2>How to Delete Your Account</h2>
                <p>You can delete your RewardsX account by following the steps outlined below. This can be done either through the app or by contacting our support team directly.</p>
                <p class="sub-title">Delete Your Account via the App</p>
                <p>You may delete your account by accessing the settings within the RewardsX app:</p>
                <ul>
                    <li>Open the RewardsX app and log into your account.</li>
                    <li>Navigate to the Account Settings section by tapping on your profile or menu.</li>
                    <li>Scroll down and click on the Delete Account option.</li>
                    <li>Follow the on-screen instructions to confirm the deletion of your account.</li>
                    <li>Once confirmed, your account and all associated personal data will be permanently deleted from our systems, except as required by law.</li>
                </ul>
                <p class="sub-title">Delete Your Account via Support</p>
                <p>If you are unable to delete your account through the app, you can submit a request to delete your account by contacting our support team:</p>
                <ul>
                    <li>Send an email to admin@credwise.ca with the subject line "Account Deletion Request."</li>
                    <li>In the email, please provide your full name, email address, and username associated with the account.</li>
                    <li>Our support team will verify your identity and process your deletion request within 30 days.</li>
                </ul>
            </section>

            <section id="section3">
                <h2>What Happens When You Delete Your Account?</h2>
                <p>Upon requesting account deletion, the following actions will be taken:</p>
                <ul>
                    <li><strong>Account Closure:</strong> Your RewardsX account will be permanently deactivated and deleted from our system.</li>
                    <li><strong>Data Deletion:</strong> All personal information associated with your account, including but not limited to your profile information, transaction history, and preferences, will be permanently erased from our servers, except where retention is required by applicable laws (e.g., for legal, regulatory, or tax purposes).</li>
                    <li><strong>Subscription & Billing:</strong> Any ongoing subscriptions or paid services will be canceled as of the account deletion date. No refunds will be issued for unused portions of any subscriptions.</li>
                    <li><strong>Service Access:</strong> Once the deletion process is complete, you will no longer have access to any services or data linked to your account. Deletion is irreversible, and your account and data cannot be recovered once deleted.</li>
                </ul>
            </section>

            <section id="section4">
                <h2>Data Retention for Legal Purposes</h2>
                <p>Please note that certain data may need to be retained by RewardsX for legal, regulatory, or security reasons even after your account has been deleted. This data will be kept in accordance with our retention policies and applicable law. Examples of data that may be retained include:</p>
                <ul>
                    <li>Financial transaction records for audit or tax purposes.</li>
                    <li>Data related to disputes, security incidents, or fraud prevention.</li>
                    <li>Records required for compliance with legal obligations.</li>
                </ul>
                <p>In such cases, the data will be securely stored and inaccessible for general use.</p>
            </section>

            <section id="section5">
                <h2>Reinstating a Deleted Account</h2>
                <p>Once your account is deleted, it cannot be reinstated or recovered. If you change your mind after initiating the deletion process, please contact our support team before the deletion is finalized, as we may be able to halt the process. Otherwise, you will need to create a new account if you wish to use RewardsX again.</p>
            </section>

